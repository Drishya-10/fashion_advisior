from sklearn.metrics.pairwise import cosine_similarity

# Build a quick lookup: product_id → index in vectors array
id_to_idx = {pid: idx for idx, pid in enumerate(ids)}

def find_similar(product_id: int, subcategory: str, top_n: int = 5) -> list:
    """
    Find the most visually similar items within a specific subcategory.
    """
    if product_id not in id_to_idx:
        print(f"No embedding for {product_id}")
        return []

    # Get query embedding
    query_vec = vectors[id_to_idx[product_id]].reshape(1, -1)

    # Get all product IDs in the target subcategory
    cat_df = df[df['subCategory'] == subcategory]
    cat_ids = [pid for pid in cat_df['id'].tolist() if pid in id_to_idx]

    if not cat_ids:
        return []

    # Get their embeddings
    cat_indices = [id_to_idx[pid] for pid in cat_ids]
    cat_vectors = vectors[cat_indices]

    # Compute cosine similarity
    scores = cosine_similarity(query_vec, cat_vectors)[0]

    # Sort by score descending
    top_indices = np.argsort(scores)[::-1][:top_n]

    results = []
    for i in top_indices:
        pid = cat_ids[i]
        row = df[df['id'] == pid].iloc[0]
        results.append({
            "id": pid,
            "name": row['productDisplayName'],
            "color": row['baseColour'],
            "score": round(float(scores[i]), 4)
        })

    return results

