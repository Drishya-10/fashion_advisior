def colors_match(color1: str, color2: str) -> bool:
    if color1 in NEUTRAL_COLORS or color2 in NEUTRAL_COLORS:
        return True
    if color1 == color2:
        return True
    return color2 in COLOR_HARMONY.get(color1, [])


def get_outfit_clip(product_id: int, top_n: int = 3) -> dict:
    """
    Rule engine + CLIP visual similarity combined.
    Rules decide WHICH categories to pair.
    CLIP decides WHICH specific items look best.
    """

    # ── 1. Find the item ──
    item_rows = df[df['id'] == product_id]
    if item_rows.empty:
        return {"error": f"Product {product_id} not found"}

    item = item_rows.iloc[0]
    sub_cat = item['subCategory']
    gender  = item['gender']
    season  = item['season']
    usage   = item['usage']
    color   = item['baseColour']

    print(f"\n👕 {item['productDisplayName']}")
    print(f"   {sub_cat} | {color} | {gender} | {season} | {usage}\n")

    compatible_cats = PAIRING_RULES.get(sub_cat, [])
    if not compatible_cats:
        return {"error": f"No rules for '{sub_cat}'"}

    outfit = {"selected_item": item.to_dict(), "suggestions": {}}

    for cat in compatible_cats:

        # ── 2. Rule-based pre-filter ──
        pool = df[df['subCategory'] == cat].copy()
        pool = pool[pool['gender'].isin([gender, 'Unisex'])]
        pool = pool[pool['season'].isin([season, 'All Season']) | pool['season'].isna()]
        pool = pool[pool['usage'] == usage]
        pool = pool[pool['baseColour'].apply(lambda c: colors_match(str(color), str(c)))]
        pool = pool.drop_duplicates(subset='productDisplayName')

        # Relax filters if pool too small
        if len(pool) < top_n:
            pool = df[df['subCategory'] == cat].copy()
            pool = pool[pool['gender'].isin([gender, 'Unisex'])]
            pool = pool[pool['baseColour'].apply(lambda c: colors_match(str(color), str(c)))]
            pool = pool.drop_duplicates(subset='productDisplayName')

        if pool.empty:
            continue

        # ── 3. CLIP similarity ranking ──
        if product_id not in id_to_idx:
            # fallback to rule-based only
            suggestions = pool.head(top_n)[
                ['id', 'productDisplayName', 'baseColour', 'usage']
            ].to_dict('records')
            outfit["suggestions"][cat] = suggestions
            continue

        query_vec = vectors[id_to_idx[product_id]].reshape(1, -1)
        pool_ids  = [pid for pid in pool['id'].tolist() if pid in id_to_idx]

        if not pool_ids:
            continue

        pool_vecs  = vectors[[id_to_idx[pid] for pid in pool_ids]]
        scores     = cosine_similarity(query_vec, pool_vecs)[0]
        top_idx    = np.argsort(scores)[::-1][:top_n]

        suggestions = []
        for i in top_idx:
            pid = pool_ids[i]
            row = df[df['id'] == pid].iloc[0]
            suggestions.append({
                "id":    pid,
                "name":  row['productDisplayName'],
                "color": row['baseColour'],
                "usage": row['usage'],
                "score": round(float(scores[i]), 4)
            })

        outfit["suggestions"][cat] = suggestions

    return outfit


# ── Pretty print ──
def display_outfit_clip(product_id: int):
    result = get_outfit_clip(product_id)
    if "error" in result:
        print(result["error"])
        return

    print("=" * 55)
    print("✨ AI STYLIST — CLIP POWERED OUTFIT")
    print("=" * 55)
    for cat, items in result["suggestions"].items():
        print(f"\n  {cat}:")
        for item in items:
            print(f"    • {item['name']} ({item['color']}) — score: {item['score']}")

