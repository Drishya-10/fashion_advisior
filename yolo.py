def detect_and_embed(image_input) -> list:
    """
    Takes an image (path, URL, or PIL Image).
    Returns list of detected items with their CLIP embeddings.
    """
    # Load image
    if isinstance(image_input, str) and image_input.startswith("http"):
        resp = requests.get(image_input)
        img_pil = Image.open(BytesIO(resp.content)).convert("RGB")
    elif isinstance(image_input, str):
        img_pil = Image.open(image_input).convert("RGB")
    else:
        img_pil = image_input.convert("RGB")

    img_array = np.array(img_pil)

    # YOLO detection
    results = yolo_model(img_pil, verbose=False)
    detections = []

    for result in results:
        boxes = result.boxes
        if boxes is None or len(boxes) == 0:
            # No detection — embed the full image as fallback
            print("⚠️ No items detected — using full image")
            embedding = _embed_image(img_pil)
            detections.append({
                "label":     "Unknown",
                "subcat":    "Topwear",
                "confidence": 0.0,
                "embedding": embedding,
                "crop":      img_pil,
            })
            continue

        for box in boxes:
            x1, y1, x2, y2 = map(int, box.xyxy[0].tolist())
            conf  = float(box.conf[0])
            cls   = int(box.cls[0])
            label = result.names[cls]

            if conf < 0.3:
                continue

            # Crop detected region
            crop = img_pil.crop((x1, y1, x2, y2))

            # Map to our subcategory
            subcat = map_yolo_label(label, cls)

            # CLIP embed the crop
            embedding = _embed_image(crop)

            detections.append({
                "label":      label,
                "subcat":     subcat,
                "confidence": round(conf, 3),
                "embedding":  embedding,
                "crop":       crop,
            })
            print(f"  Detected: {label} → {subcat} (conf: {conf:.2f})")

    return detections


def _embed_image(img_pil: Image.Image) -> np.ndarray:
    """Generate CLIP embedding for a PIL image."""
    tensor = preprocess(img_pil).unsqueeze(0).to(device)
    with torch.no_grad():
        emb = clip_model.encode_image(tensor)
        emb = emb / emb.norm(dim=-1, keepdim=True)
    return emb.cpu().numpy()[0]

