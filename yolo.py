PAIRING_RULES = {
    "Topwear":    ["Bottomwear", "Shoes", "Sandal", "Watches", "Belts",
                   "Bags", "Eyewear", "Socks", "Ties", "Scarves"],
    "Bottomwear": ["Topwear", "Shoes", "Sandal", "Belts", "Socks",
                   "Watches", "Bags", "Eyewear"],
    "Dress":      ["Shoes", "Sandal", "Bags", "Jewellery",
                   "Watches", "Eyewear", "Scarves"],
    "Shoes":      ["Topwear", "Bottomwear", "Socks", "Watches", "Bags"],
    "Bags":       ["Topwear", "Bottomwear", "Shoes", "Watches", "Eyewear"],
}

def recommend_from_image(image_input, gender: str = None, usage: str = None) -> dict:
    """
    Full pipeline:
    Image → YOLO detect → CLIP embed → find match → pair outfit
    """
    print("\n🔍 Detecting clothing items...")
    detections = detect_and_embed(image_input)

    if not detections:
        return {"error": "No clothing detected in image"}

    # Use highest confidence detection as base
    base = max(detections, key=lambda x: x['confidence'])
    print(f"\n👕 Base item detected: {base['label']} → {base['subcat']}")

    # Find most similar item in our dataset
    print("\n🔎 Finding closest match in dataset...")
    matches = find_similar_in_dataset(
        base['embedding'],
        subcategory=base['subcat'],
        gender=gender,
        usage=usage,
        top_n=1
    )

    if not matches:
        return {"error": f"No matches found for {base['subcat']}"}

    matched_item = matches[0]
    matched_id   = matched_item['id']
    print(f"✅ Matched: {matched_item['name']} (score: {matched_item['score']})")

    # Now run pairing engine on matched item
    print("\n👗 Building outfit recommendations...")
    outfit = {"detected": base['label'],
              "matched_item": matched_item,
              "suggestions": {}}

    for cat in PAIRING_RULES.get(base['subcat'], []):
        similar = find_similar_in_dataset(
            vectors[id_to_idx[matched_id]],
            subcategory=cat,
            gender=gender,
            usage=usage,
            top_n=3
        )
        if similar:
            outfit["suggestions"][cat] = similar

    return outfit


def display_recommendation(result: dict):
    if "error" in result:
        print(f"❌ {result['error']}")
        return

    print("\n" + "="*55)
    print("✨ AI STYLIST — IMAGE BASED RECOMMENDATION")
    print("="*55)
    print(f"\n📸 Detected: {result['detected']}")
    print(f"🎯 Matched:  {result['matched_item']['name']} ({result['matched_item']['color']})")
    print(f"   Similarity score: {result['matched_item']['score']}")
    print("\n👗 Complete outfit:\n")
    for cat, items in result["suggestions"].items():
        print(f"  {cat}:")
        for item in items:
            print(f"    • {item['name']} ({item['color']}) — {item['score']}")

