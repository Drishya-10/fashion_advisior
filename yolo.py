def find_similar_in_dataset(
    embedding: np.ndarray,
    subcategory: str,
    gender: str = None,
    usage: str = None,
    top_n: int = 5
) -> list:
    """
    Given a CLIP embedding, find most similar items
    in our dataset within a specific subcategory.
    """
    pool = df[df['subCategory'] == subcategory].copy()

    if gender:
        pool = pool[pool['gender'].isin([gender, 'Unisex'])]
    if usage:
        pool = pool[pool['usage'] == usage]

    pool = pool.drop_duplicates(subset='productDisplayName')

    pool_ids = [pid for pid in pool['id'].tolist() if pid in id_to_idx]
    if not pool_ids:
        return []

    pool_vecs = vectors[[id_to_idx[pid] for pid in pool_ids]]
    scores    = cosine_similarity(embedding.reshape(1, -1), pool_vecs)[0]
    top_idx   = np.argsort(scores)[::-1][:top_n]

    results = []
    for i in top_idx:
        pid = pool_ids[i]
        row = df[df['id'] == pid].iloc[0]
        results.append({
            "id":    int(pid),
            "name":  row['productDisplayName'],
            "color": row['baseColour'],
            "subcat": row['subCategory'],
            "score": round(float(scores[i]), 4),
        })

    return results

