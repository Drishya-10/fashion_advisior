# ── Color harmony rules ──
# Neutrals go with everything. Bright colors need careful pairing.

NEUTRAL_COLORS = {"Black", "White", "Grey", "Navy Blue", "Beige", "Cream",
                  "Off White", "Charcoal", "Khaki", "Brown", "Tan"}

COLOR_HARMONY = {
    "Red":        ["Black", "White", "Navy Blue", "Grey", "Beige"],
    "Blue":       ["White", "Grey", "Black", "Beige", "Brown"],
    "Green":      ["White", "Black", "Beige", "Brown", "Khaki"],
    "Yellow":     ["White", "Black", "Grey", "Navy Blue"],
    "Pink":       ["White", "Black", "Grey", "Navy Blue", "Beige"],
    "Orange":     ["White", "Black", "Navy Blue", "Brown"],
    "Purple":     ["White", "Black", "Grey", "Beige"],
    "Maroon":     ["White", "Black", "Beige", "Khaki"],
    "Mustard":    ["White", "Black", "Brown", "Beige", "Navy Blue"],
    "Rust":       ["White", "Black", "Brown", "Beige", "Khaki"],
    "Turquoise":  ["White", "Black", "Navy Blue", "Beige"],
    "Lavender":   ["White", "Grey", "Black", "Beige"],
}

def colors_match(color1: str, color2: str) -> bool:
    """Return True if two colors are compatible."""
    if color1 in NEUTRAL_COLORS or color2 in NEUTRAL_COLORS:
        return True  # neutrals always work
    if color1 == color2:
        return True  # monochrome always works
    harmony = COLOR_HARMONY.get(color1, [])
    return color2 in harmony

