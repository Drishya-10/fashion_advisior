def get_outfit(product_id: int, n: int = 3) -> dict:
    """
    Given a product ID, return compatible outfit suggestions.
    n = number of suggestions per category
    """

    # ── 1. Find the item ──
    item_rows = df[df['id'] == product_id]
    if item_rows.empty:
        return {"error": f"Product ID {product_id} not found"}

    item = item_rows.iloc[0]
    sub_cat   = item['subCategory']
    gender    = item['gender']
    season    = item['season']
    usage     = item['usage']
    color     = item['baseColour']

    print(f"\n👕 Selected: {item['productDisplayName']}")
    print(f"   Category: {sub_cat} | Color: {color} | Gender: {gender}")
    print(f"   Season: {season} | Usage: {usage}\n")

    # ── 2. Get compatible categories ──
    compatible_cats = PAIRING_RULES.get(sub_cat, [])
    if not compatible_cats:
        return {"error": f"No pairing rules found for '{sub_cat}'"}

    outfit = {"selected_item": item.to_dict(), "suggestions": {}}

    # ── 3. Filter & score for each compatible category ──
    for cat in compatible_cats:
        pool = df[df['subCategory'] == cat].copy()

        if pool.empty:
            continue

        # Layer 2a — gender filter
        pool = pool[pool['gender'].isin([gender, 'Unisex'])]

        # Layer 2b — season filter (allow NaN seasons)
        pool = pool[pool['season'].isin([season, 'All Season']) | pool['season'].isna()]

        # Layer 2c — usage filter
        pool = pool[pool['usage'] == usage]

        # Layer 2d — color harmony
        pool = pool[pool['baseColour'].apply(
            lambda c: colors_match(str(color), str(c))
        )]

        if pool.empty:
            # Relax usage filter if nothing found
            pool = df[df['subCategory'] == cat].copy()
            pool = pool[pool['gender'].isin([gender, 'Unisex'])]
            pool = pool[pool['baseColour'].apply(
                lambda c: colors_match(str(color), str(c))
            )]

        if pool.empty:
            continue

        # Pick top n
        # Replace pool.head(n) with this in your get_outfit function
        suggestions = pool.drop_duplicates(subset='productDisplayName').head(n)[
            ['id', 'productDisplayName', 'baseColour', 'usage', 'season']
        ].to_dict('records')
        outfit["suggestions"][cat] = suggestions

    return outfit


