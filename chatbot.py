conversation_history = []

def chat(user_message: str, base_product_id: int = None) -> str:
    global conversation_history

    # 1. Extract intent
    intent = extract_intent(user_message)
    print(f"🧠 Intent: {intent}")

    # 2. Get suggestions
    outfit = suggest_outfit(intent, base_product_id)
    if "error" in outfit:
        return f"Sorry — {outfit['error']}. Could you give me more details?"

    # 3. Build outfit summary for LLM
    summary = f"Base item: {outfit['base_item']['name']} ({outfit['base_item']['color']})\n"
    for cat, items in outfit["suggestions"].items():
        summary += f"\n{cat}:\n"
        for item in items:
            summary += f"  - {item['name']} ({item['color']})\n"

    # 4. LLM formats naturally
    system_prompt = """
You are a warm, confident AI fashion stylist named Zara.
Given the user's request and outfit suggestions, respond naturally like a personal stylist.
Be concise and specific — mention actual item names.
End every response with one short question asking if they want to adjust anything.
Never mention scores or technical details.
"""

    conversation_history.append({"role": "user", "content": user_message})

    messages = (
        [{"role": "system", "content": system_prompt}]
        + conversation_history
        + [{"role": "user", "content": f"Outfit suggestions to present:\n{summary}"}]
    )

    reply = llm(messages, temperature=0.7)
    conversation_history.append({"role": "assistant", "content": reply})

    chat.last_outfit = outfit
    return reply

