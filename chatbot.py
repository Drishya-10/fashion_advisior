from collections import defaultdict
from sklearn.metrics.pairwise import cosine_similarity

preference_store = defaultdict(float)

LIKE_WEIGHT    =  0.3
DISLIKE_WEIGHT = -0.3

def give_feedback(product_id: int, feedback: str):
    if feedback == 'like':
        preference_store[product_id] += LIKE_WEIGHT
        _propagate_feedback(product_id, weight=0.1)
    elif feedback == 'dislike':
        preference_store[product_id] += DISLIKE_WEIGHT
        _propagate_feedback(product_id, weight=-0.1)

def _propagate_feedback(product_id: int, weight: float, top_k: int = 5):
    if product_id not in id_to_idx:
        return
    query_vec = vectors[id_to_idx[product_id]].reshape(1, -1)
    scores    = cosine_similarity(query_vec, vectors)[0]
    top_idx   = np.argsort(scores)[::-1][1:top_k+1]
    for idx in top_idx:
        preference_store[int(ids[idx])] += weight

def adjusted_score(product_id: int, clip_score: float) -> float:
    return clip_score + preference_store.get(product_id, 0.0)

