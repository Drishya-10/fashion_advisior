PAIRING_RULES = {
    "Topwear":    ["Bottomwear", "Shoes", "Sandal", "Watches", "Belts",
                   "Bags", "Eyewear", "Socks", "Ties", "Scarves", "Headwear"],
    "Bottomwear": ["Topwear", "Shoes", "Sandal", "Belts", "Socks",
                   "Watches", "Bags", "Eyewear"],
    "Dress":      ["Shoes", "Sandal", "Bags", "Jewellery", "Watches",
                   "Eyewear", "Scarves", "Headwear"],
    "Saree":      ["Shoes", "Sandal", "Bags", "Jewellery",
                   "Watches", "Eyewear"],
    "Shoes":      ["Topwear", "Bottomwear", "Socks", "Watches", "Bags"],
}

NEUTRAL_COLORS = {"Black", "White", "Grey", "Navy Blue", "Beige",
                  "Cream", "Off White", "Charcoal", "Khaki", "Brown", "Tan"}

def colors_match(c1, c2):
    if c1 in NEUTRAL_COLORS or c2 in NEUTRAL_COLORS:
        return True
    return c1 == c2

def suggest_outfit(intent: dict, base_product_id: int = None, top_n: int = 3) -> dict:
    gender = intent.get('gender')
    usage  = intent.get('usage')
    season = intent.get('season')

    # Find base item if not provided
    if base_product_id is None:
        pool = df.copy()
        if gender: pool = pool[pool['gender'].isin([gender, 'Unisex'])]
        if usage:  pool = pool[pool['usage'] == usage]
        if season: pool = pool[pool['season'] == season]
        pool = pool[pool['subCategory'].isin(PAIRING_RULES.keys())]
        if pool.empty:
            return {"error": "No matching base item found"}
        base_row = pool.sample(1).iloc[0]
        base_product_id = int(base_row['id'])
    else:
        rows = df[df['id'] == base_product_id]
        if rows.empty:
            return {"error": "Product not found"}
        base_row = rows.iloc[0]

    sub_cat = base_row['subCategory']
    color   = base_row['baseColour']
    gender  = gender or base_row['gender']
    usage   = usage  or base_row['usage']
    season  = season or base_row['season']

    outfit = {
        "base_item": {
            "id":       int(base_product_id),
            "name":     base_row['productDisplayName'],
            "color":    color,
            "category": sub_cat,
        },
        "suggestions": {}
    }

    for cat in PAIRING_RULES.get(sub_cat, []):
        pool = df[df['subCategory'] == cat].copy()
        pool = pool[pool['gender'].isin([gender, 'Unisex'])]
        if usage:  pool = pool[pool['usage'] == usage]
        if season: pool = pool[pool['season'].isin([season, 'All Season']) | pool['season'].isna()]
        pool = pool[pool['baseColour'].apply(lambda c: colors_match(str(color), str(c)))]
        pool = pool.drop_duplicates(subset='productDisplayName')

        if len(pool) < top_n:
            pool = df[df['subCategory'] == cat].copy()
            pool = pool[pool['gender'].isin([gender, 'Unisex'])]
            pool = pool[pool['baseColour'].apply(lambda c: colors_match(str(color), str(c)))]
            pool = pool.drop_duplicates(subset='productDisplayName')

        if pool.empty or base_product_id not in id_to_idx:
            continue

        query_vec   = vectors[id_to_idx[base_product_id]].reshape(1, -1)
        pool_ids    = [pid for pid in pool['id'].tolist() if pid in id_to_idx]
        if not pool_ids:
            continue

        pool_vecs   = vectors[[id_to_idx[pid] for pid in pool_ids]]
        clip_scores = cosine_similarity(query_vec, pool_vecs)[0]

        # RL — adjust scores by preference
        final_scores = np.array([
            adjusted_score(pid, score)
            for pid, score in zip(pool_ids, clip_scores)
        ])

        top_idx = np.argsort(final_scores)[::-1][:top_n]
        suggestions = []
        for i in top_idx:
            pid = pool_ids[i]
            row = df[df['id'] == pid].iloc[0]
            suggestions.append({
                "id":    int(pid),
                "name":  row['productDisplayName'],
                "color": row['baseColour'],
                "score": round(float(final_scores[i]), 4)
            })

        outfit["suggestions"][cat] = suggestions

    return outfit

